
public class Candidato {
		
		String código_votacao;
		String nome_candidato;
		String partido;
		int num_votos = 0;
		
		
		public String getCódigo_votacao() {
			return código_votacao;
		}
		public void setCódigo_votacao(String código_votacao) {
			this.código_votacao = código_votacao;
		}
		public String getNome_candidato() {
			return nome_candidato;
		}
		public void setNome_candidato(String nome_candidato) {
			this.nome_candidato = nome_candidato;
		}
		public String getPartido() {
			return partido;
		}
		public void setPartido(String partido) {
			this.partido = partido;
		}
		public int getNum_votos() {
			return num_votos;
		}
		public void setNum_votos(int num_votos) {
			this.num_votos = num_votos;
		}
		
		
		
		
	}
